export const mergeSiteContent = (remoteItems = []) => {
  const remoteByKey = new Map(remoteItems.map((item) => [item.key, item]));
  const mergedDefaults = defaultSiteContent.map((item) => ({
    ...item,
    ...remoteByKey.get(item.key),
  }));

  const defaultKeys = new Set(defaultSiteContent.map((item) => item.key));
  const customItems = remoteItems.filter((item) => !defaultKeys.has(item.key));

  return [...mergedDefaults, ...customItems].sort((first, second) => {
    if (first.section !== second.section) return first.section.localeCompare(second.section);
    return (first.sort_order || 0) - (second.sort_order || 0);
  });
};

export const getSiteContentItem = (items = [], key, fallback = '') => {
  const item = items.find((candidate) => candidate.key === key);
  if (!item || item.visible === false) return fallback;
  return item.label || fallback;
};
